#include <unistd.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	x;
	int	j;

	i = 0;
	j = size - 1;
	x = 0;
	while (i < j)
	{
		x = tab[j];
		tab[j] = tab[i];
		tab[i] = x;
		i = i + 1;
		j = j - 1;
	}
}
